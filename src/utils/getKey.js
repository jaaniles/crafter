export default function getKey(i) {
  switch (i) {
    case 0:
      return "w";
    case 1:
      return "a";
    case 2:
      return "d";
    case 3:
      return "x";
    case 4:
      return "s";
    case 5:
      return "q";
    case 6:
      return "e";
    case 7:
      return "z";
    case 8:
      return "c";
    default:
      return "f";
  }
}
