export default function getInventoryKey(i) {
  switch (i) {
    case 0:
      return "r";
    case 1:
      return "f";
    case 2:
      return "v";
    case 3:
      return "t";
    case 4:
      return "g";
    case 5:
      return "b";
    case 6:
      return "y";
    case 7:
      return "h";
    case 8:
      return "n";
    default:
      return "u";
  }
}
